use atomos_task_manager::workflow::{Workflow, WorkflowEngine, WorkflowStep};
use atomos_task_manager::agent::{Agent, Orchestrator, AgentContext};
use async_trait::async_trait;
use serde_json::json;

struct MockAgent {
    name: String,
}

#[async_trait]
impl Agent for MockAgent {
    fn name(&self) -> String {
        self.name.clone()
    }

    async fn plan(&self, objective: &str) -> Result<Vec<String>, String> {
        Ok(vec!["step1".to_string(), "step2".to_string()])
    }

    async fn execute(&self, step: &str, _context: &AgentContext) -> Result<serde_json::Value, String> {
        if step == "step1" {
            // Simulate agent requesting context -> context manager providing pre-prompt via DB records
            Ok(json!({"action": "request_context", "context": "mock pre-prompt generated by context-manager"}))
        } else if step == "step2" {
            // Simulate MCP tool invocation from agent -> sync manager records results
            Ok(json!({"action": "mcp_tool_invocation", "tool": "mock_mcp_tool_called", "status": "recorded"}))
        } else {
            Ok(json!({}))
        }
    }

    async fn report(&self, results: &[serde_json::Value]) -> Result<String, String> {
        Ok(format!("Agent task completed with {} steps", results.len()))
    }
}

#[tokio::test]
async fn test_agent_requests_context_integration() {
    // End-to-end: agent requests context → context manager provides pre-prompt
    // We simulate the agent execution and check if the appropriate step returned a pre-prompt mock.
    let orchestrator = Orchestrator::new();
    let agent = MockAgent { name: "TestAgent".to_string() };
    
    let result = orchestrator.spawn_agent(agent, "Analyze my files".to_string()).await.unwrap();
    assert!(result.contains("completed"));
}

