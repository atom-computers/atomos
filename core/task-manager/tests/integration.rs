#[tokio::test]
async fn test_workflow_stores_results_for_rag() {
    // End-to-end: workflow step stores results → context manager can retrieve via RAG
    let mut workflow = Workflow::new("rag_workflow", "write_results");
    workflow.add_step(WorkflowStep {
        id: "write_results".to_string(),
        name: "Write Results".to_string(),
        action: "store_data".to_string(),
        inputs: json!({"data": "Sample analysis"}),
        next_steps: vec![],
    });

    let mut engine = WorkflowEngine::new(None); // Use Mock DB for test
    engine.execute(&mut workflow).await.unwrap();
    
    let res = workflow.results.get("write_results").unwrap();
    assert_eq!(res["status"], "executed");
    // In a real integration, Context Manager would pull this execution result from SurrealDB.
}

