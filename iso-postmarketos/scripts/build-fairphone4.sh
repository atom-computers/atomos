##############################################################################
# Step 2.5: re-run user-touching post-install scripts inside a privileged chroot.
#
# `apk --root /target add` runs post-installs via QEMU user-mode emulation
# during package install. That works for most scripts, but two classes
# routinely misbehave:
#
#   1. Post-installs that touch user accounts (usermod -aG ...): the
#      target user (UID 10000) was created by us AFTER the apk add
#      transaction, so anything that ran usermod against that uid during
#      the apk pass was a no-op silently.
#
#   2. Post-installs that depend on running services or sockets being
#      reachable: the qemu-user emulation layer cannot start daemons,
#      so any post-install relying on dbus / elogind being live during
#      install is skipped.
#
# postmarketos-ui-phosh.post-install does exactly case 1:
#   default_user=$(getent passwd "10000" | cut -d: -f1)
#   if [ -n "$default_user" ]; then
#       usermod -aG feedbackd "$default_user"
#   fi
# When this ran during apk install, the user did not exist yet, so the
# usermod was a no-op. We re-run those scripts here against the now-
# populated rootfs so the user lands in feedbackd / video / render /
# seat / wheel / netdev / dialout / bluetooth groups for real.
##############################################################################
echo "=== build-fairphone4: re-run user-touching post-install scripts in chroot ==="
"$ENGINE" run --rm --privileged --platform "linux/arm64" \
    -v "$ROOTFS_VOLUME:/target" \
    "$ALPINE_IMAGE" /bin/sh -eu -c '
        for fs in proc sys dev dev/pts; do mkdir -p /target/$fs; done
        mount -t proc proc        /target/proc
        mount -t sysfs sysfs      /target/sys
        mount --bind /dev         /target/dev
        mount --bind /dev/pts     /target/dev/pts 2>/dev/null || mount -t devpts devpts /target/dev/pts
        cleanup() {
            umount /target/dev/pts 2>/dev/null || true
            umount /target/dev     2>/dev/null || true
            umount /target/sys     2>/dev/null || true
            umount /target/proc    2>/dev/null || true
        }
        trap cleanup EXIT INT TERM

        # ---- ensure system users created by post-install scripts exist ----
        # Alpine s `greetd` apk creates a system user `greetd:greetd`
        # via post-install. When apk --root /target add runs that
        # post-install via QEMU user-mode emulation, the addgroup/adduser
        # calls sometimes silently fail to write to /target/etc/passwd
        # (the binfmt layer cannot reliably acquire the lockfile under
        # heavy parallel install). When the user is missing, greetd
        # exits at startup with "ERROR: user.greetd failed to start" --
        # that exact message format comes from greetd reporting the
        # named system user it could not setuid() to.
        #
        # Create it explicitly here as a system user (UID < 1000), with
        # the conventions Alpine uses: locked password, /sbin/nologin
        # shell, /var/lib/greetd home. Idempotent: skip if already there.
        # HARD-FAIL with diagnostic if creation does not succeed --
        # silently shipping a rootfs without the greetd user causes the
        # exact "ERROR: user.greetd failed to start" boot loop the user
        # has been hitting.
        if ! chroot /target /bin/sh -c "getent group greetd >/dev/null 2>&1"; then
            echo "fixup: creating greetd system group"
            if ! chroot /target /bin/sh -c "addgroup -S greetd"; then
                echo "FATAL: addgroup -S greetd failed inside chroot." >&2
                echo "       /target/etc/group head:" >&2
                head -20 /target/etc/group >&2
                exit 11
            fi
        fi
        if ! chroot /target /bin/sh -c "getent passwd greetd >/dev/null 2>&1"; then
            echo "fixup: creating greetd system user"
            # Alpine busybox adduser flags: -S system, -D no password,
            # -H no home dir create, -G primary group, -s shell.
            if ! chroot /target /bin/sh -c "
                mkdir -p /var/lib/greetd
                adduser -S -D -H -G greetd -h /var/lib/greetd -s /sbin/nologin greetd
                chown -R greetd:greetd /var/lib/greetd
                chmod 0755 /var/lib/greetd
            "; then
                echo "FATAL: adduser greetd failed inside chroot." >&2
                echo "       /target/etc/passwd head:" >&2
                head -20 /target/etc/passwd >&2
                exit 12
            fi
        fi
        # Re-verify creation actually persisted to /target/etc/passwd
        # (busybox adduser sometimes returns 0 but writes nothing when
        # the lockfile path is unwriteable from the qemu-emulated env).
        if ! chroot /target /bin/sh -c "getent passwd greetd >/dev/null 2>&1"; then
            echo "FATAL: greetd account still missing AFTER addgroup+adduser succeeded." >&2
            echo "       This is the bug that produces ERROR: user.greetd failed to start at boot." >&2
            echo "       /target/etc/passwd grep:" >&2
            grep -i greetd /target/etc/passwd /target/etc/group /target/etc/shadow >&2 || true
            echo "       /target permissions:" >&2
            ls -la /target/etc/{passwd,group,shadow} >&2 || true
            exit 13
        fi
        echo "fixup: greetd account confirmed in /target/etc/passwd:"
        chroot /target /bin/sh -c "getent passwd greetd; getent group greetd" | sed "s/^/  /"

        # Add greetd user to seat/video/input groups so the greeter
        # process can drive the wlroots compositor.
        for grp in seat video input render; do
            if chroot /target /bin/sh -c "getent group $grp >/dev/null 2>&1"; then
                chroot /target /bin/sh -c "addgroup greetd $grp" >/dev/null 2>&1 || true
            fi
        done

        # Re-run any cached apk post-install script that touches the
        # default user. apk stores the post-install scripts it ran during
        # `apk add` under /lib/apk/scripts/<pkg>-<ver>.post-install.
        # Re-running them is safe (usermod -aG is idempotent).
        for f in /target/lib/apk/scripts/postmarketos-ui-phosh-*.post-install \
                 /target/var/lib/apk/scripts/postmarketos-ui-phosh-*.post-install \
                 /target/lib/apk/scripts/postmarketos-base-ui-*.post-install; do
            [ -f "$f" ] || continue
            rel="${f#/target}"
            echo "rerun: $rel"
            chroot /target sh "$rel" 2>&1 | sed "s/^/  /" || echo "  (post-install rc=$? ignored)" >&2
        done

        # Force-add the user to canonical groups via in-chroot usermod,
        # in case our /etc/group hand-edit conflicted with apk group
        # creation timing (e.g. apk created the video group with a
        # specific gid AFTER we hand-appended the user to it).
        for grp in wheel video render audio input plugdev seat netdev dialout bluetooth feedbackd; do
            if chroot /target /bin/sh -c "getent group $grp >/dev/null 2>&1"; then
                # busybox addgroup syntax: addgroup <user> <group>
                chroot /target /bin/sh -c "addgroup user $grp" >/dev/null 2>&1 || true
                # GNU shadow usermod syntax: usermod -aG <group> <user>
                chroot /target /bin/sh -c "usermod -aG $grp user" >/dev/null 2>&1 || true
            fi
        done

        echo "build-fairphone4: final user identity in rootfs:"
        chroot /target /bin/sh -c "id user" >&2
        echo "build-fairphone4: greetd system user in rootfs:"
        chroot /target /bin/sh -c "id greetd 2>&1 || true" >&2
    '

