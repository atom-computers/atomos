# Meson incremental cache (same as build-qemu.sh; named docker volume by
# default for macOS bind-mount EPERM resilience even though build-qemu
# already requires Linux — keeps the option open).
if [ -n "$MESON_CACHE_HOST_DIR" ]; then
    MESON_CACHE_MOUNT="$MESON_CACHE_HOST_DIR"
    MESON_CACHE_KIND="host directory: $MESON_CACHE_HOST_DIR"
