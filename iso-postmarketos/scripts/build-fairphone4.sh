        ln -sf ../../init.d/bluetooth        /target/etc/runlevels/default/bluetooth || true
        ln -sf ../../init.d/iio-sensor-proxy /target/etc/runlevels/default/iio-sensor-proxy || true
        ln -sf ../../init.d/apk-polkit-server /target/etc/runlevels/default/apk-polkit-server || true
        ln -sf ../../init.d/elogind          /target/etc/runlevels/default/elogind || true
        ln -sf ../../init.d/sleep-inhibitor  /target/etc/runlevels/default/sleep-inhibitor || true
        ln -sf ../../init.d/openrc-settingsd /target/etc/runlevels/default/openrc-settingsd || true
        ln -sf ../../init.d/modemmanager     /target/etc/runlevels/default/modemmanager || true
        ln -sf ../../init.d/networkmanager   /target/etc/runlevels/default/networkmanager || true
        ln -sf ../../init.d/wpa_supplicant   /target/etc/runlevels/default/wpa_supplicant || true
        ln -sf ../../init.d/zram-init        /target/etc/runlevels/default/zram-init || true
        ln -sf ../../init.d/sshd             /target/etc/runlevels/default/sshd || true
        ln -sf ../../init.d/seatd            /target/etc/runlevels/default/seatd || true
        ln -sf ../../init.d/greetd           /target/etc/runlevels/default/greetd || true
