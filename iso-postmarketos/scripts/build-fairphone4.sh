        # (already shipped by the apk), wireplumber rule, and udev rule.
        # The apk install handles this when /etc/init.d hooks fire, but
        # apk --root mode skips trigger scripts; re-run the file install
        # manually here so the rootfs ships them.
        if [ -d /target/usr/share/alsa/ucm2/Fairphone/fp4 ]; then
            : # already shipped by device-fairphone-fp4
        fi

        # Create XDG_RUNTIME_DIR for the user session.
        mkdir -p "/target/run/user/${PMOS_USER_UID}"
        chmod 700 "/target/run/user/${PMOS_USER_UID}"

        # Hash the install password.
        apk add --no-cache openssl >/dev/null 2>&1 || true
        pass_hash="$(openssl passwd -6 "$INSTALL_PASSWORD" 2>/dev/null || true)"
