#!/bin/bash
# build-fairphone4.sh — pmbootstrap-free Fairphone 4 image builder.
#
# Same model as scripts/build-qemu.sh: bootstrap an aarch64 rootfs into a
# docker/podman volume, install Alpine + postmarketOS APKs directly via
# `apk --root /target add`, build the AtomOS Rust + custom phosh
# components in a heavy container, install AtomOS overlays directly, and
# emit fastboot-flashable artifacts (boot.img + sparse fairphone-fp4.img)
# under build/host-export-fairphone-fp4/.
#
# What changed from build-image.sh:
#   - No pmbootstrap. Everything happens via Alpine + docker/podman the
#     same way build-qemu.sh works for the QEMU profile.
#   - postmarketOS APKs (device-fairphone-fp4, firmware-fairphone-fp4-*,
#     linux-postmarketos-qcom-sm6350, hexagonrpcd, mkbootimg,
#     make-dynpart-mappings, soc-qcom*, postmarketos-base) come from
#     the official pmOS mirror at https://mirror.postmarketos.org/...
#     The pmOS signing key is vendored at
#     pmaports/main/postmarketos-keys/build.postmarketos.org.rsa.pub.
#   - boot.img is generated by `mkbootimg` using the offsets/pagesize
#     from pmaports/device/community/device-fairphone-fp4/deviceinfo
#     (DTB appended to kernel because deviceinfo_append_dtb="true").
#   - Rootfs is packed into a sparse ext4 image suitable for
#     `fastboot flash userdata fairphone-fp4.img` (or super-partition
#     payload, depending on device-side workflow).
#
# Usage:
#   bash scripts/build-fairphone4.sh [profile-env] \
#       [--without-overview-chat-ui] [--without-home-bg]
#
# Output:
#   build/host-export-fairphone-fp4/boot.img
#   build/host-export-fairphone-fp4/fairphone-fp4.img
#
# Environment overrides:
#   ATOMOS_FP4_ALPINE_CONTAINER_IMAGE   base image (default alpine:edge)
#   ATOMOS_FP4_PMOS_CHANNEL             pmOS channel/branch (default master)
#   ATOMOS_FP4_PMOS_MIRROR              pmOS mirror URL
#                                       (default https://mirror.postmarketos.org/postmarketos/)
#   ATOMOS_FP4_MESON_CACHE_HOST_DIR     host bind mount for /cache (else
#                                       named docker volume)
#   ATOMOS_FP4_MESON_CACHE_CLEAN=1      wipe meson/ccache cache
#   ATOMOS_FP4_BUILD_ENGINE             docker | podman (auto-detected)
#   ATOMOS_FP4_ROOTFS_SIZE_MB           pack target for fairphone-fp4.img
#                                       (default: rootfs size + 256 MiB)
#   ATOMOS_FP4_KEEP_ROOTFS_VOLUME=1     do not remove the rootfs volume
#                                       on exit (debug)
set -euo pipefail

usage() {
    cat >&2 <<'EOF'
Usage: build-fairphone4.sh [profile-env] [--without-overview-chat-ui] [--without-home-bg] [--without-app-switcher]

Builds a fastboot-flashable Fairphone 4 image (boot.img + fairphone-fp4.img)
from Alpine Linux + the postmarketOS APK mirror, without pmbootstrap.

Options:
  --without-overview-chat-ui    Skip building/installing atomos-overview-chat-ui.
  --without-home-bg             Skip building/installing atomos-home-bg.
  --without-app-switcher        Skip building/installing atomos-app-handler.
                                Legacy --without-swipe-bridge accepted (alias).

Outputs:
  build/host-export-fairphone-fp4/boot.img
  build/host-export-fairphone-fp4/fairphone-fp4.img
EOF
}

PROFILE_ENV=""
BUILD_OVERVIEW_CHAT_UI=1
BUILD_HOME_BG=1
BUILD_APP_HANDLER=1
while [ "$#" -gt 0 ]; do
    case "$1" in
        --without-overview-chat-ui|--skip-overview-chat-ui)
            BUILD_OVERVIEW_CHAT_UI=0
            ;;
        --without-home-bg|--skip-home-bg)
            BUILD_HOME_BG=0
            ;;
        --without-app-switcher|--skip-app-switcher|--without-swipe-bridge|--skip-swipe-bridge)
            BUILD_APP_HANDLER=0
            ;;
        -h|--help)
            usage
            exit 0
            ;;
        -*)
            echo "ERROR: unknown option: $1" >&2
            usage
            exit 1
            ;;
        *)
            if [ -n "$PROFILE_ENV" ]; then
                echo "ERROR: profile env provided more than once: $1" >&2
                usage
                exit 1
            fi
            PROFILE_ENV="$1"
            ;;
    esac
    shift
done
PROFILE_ENV="${PROFILE_ENV:-config/fairphone-fp4.env}"

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

