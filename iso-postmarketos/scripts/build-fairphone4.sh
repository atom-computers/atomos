        # postmarketos-ui-phosh _pmb_groups + pmaports per-UI extras.
        # Critical entries:
        #   wheel     - sudo
        #   video     - /dev/dri/card0 (DRI rendering)
        #   render    - /dev/dri/renderD128 (GPU compute; phoc/wlroots
