echo "=== build-fairphone4: export boot.img ==="
"$ENGINE" run --rm --platform "linux/arm64" \
    -v "$ROOTFS_VOLUME:/target" \
    -v "$EXPORT_DIR:/exportdir" \
    -e DEVICE_DTB="$DEVICE_DTB" \
    -e DEVICE_APPEND_DTB="$DEVICE_APPEND_DTB" \
    -e DEVICE_PAGESIZE="$DEVICE_PAGESIZE" \
    -e DEVICE_BASE="$DEVICE_BASE" \
    -e DEVICE_KOFF="$DEVICE_KOFF" \
    -e DEVICE_ROFF="$DEVICE_ROFF" \
    -e DEVICE_SOFF="$DEVICE_SOFF" \
    -e DEVICE_TOFF="$DEVICE_TOFF" \
    -e DEVICE_KERNEL_CMDLINE="$DEVICE_KERNEL_CMDLINE" \
    "$ALPINE_IMAGE" /bin/sh -eu -c '
        # ---- boot-deploy output preferred path -------------------------
        # boot-deploy convention: /boot/boot.img-<flavor> for the per-kernel
        # image and /boot/boot.img as a generic copy. When both exist the
        # generic /boot/boot.img is the one to ship.
        for cand in \
            /target/boot/boot.img \
            /target/boot/boot.img-postmarketos-qcom-sm6350 \
            /target/boot/boot.img-pmos; do
            if [ -f "$cand" ]; then
                echo "build-fairphone4: using boot-deploy artifact: $cand"
                cp -f "$cand" /exportdir/boot.img
                ls -la /exportdir/boot.img
                exit 0
            fi
        done
        boot_img_glob="$(ls /target/boot/boot.img* 2>/dev/null | head -n 1 || true)"
        if [ -n "$boot_img_glob" ] && [ -f "$boot_img_glob" ]; then
            echo "build-fairphone4: using boot-deploy artifact (glob): $boot_img_glob"
            cp -f "$boot_img_glob" /exportdir/boot.img
            ls -la /exportdir/boot.img
            exit 0
        fi

        echo "build-fairphone4: no boot-deploy boot.img found; falling back to manual mkbootimg." >&2

        # ---- manual mkbootimg fallback ---------------------------------
        # Locate the kernel image. linux-postmarketos-qcom-sm6350 installs
        # to /boot/vmlinuz-postmarketos-qcom-sm6350 (Alpine convention).
        KERNEL=""
        for k in /target/boot/vmlinuz-postmarketos-qcom-sm6350 \
                 /target/boot/vmlinuz-pmos \
                 /target/boot/vmlinuz; do
            if [ -f "$k" ]; then KERNEL="$k"; break; fi
        done
        if [ -z "$KERNEL" ]; then
            KERNEL="$(ls /target/boot/vmlinuz* 2>/dev/null | head -n 1 || true)"
        fi
        if [ -z "$KERNEL" ] || [ ! -f "$KERNEL" ]; then
            echo "ERROR: no kernel image under /target/boot/" >&2
            ls -la /target/boot/ >&2 || true
            exit 1
        fi
        echo "build-fairphone4: kernel: $KERNEL"

        # Locate the initramfs (postmarketos-mkinitfs writes
        # initramfs-postmarketos-qcom-sm6350 in /boot).
        INITRAMFS=""
        for r in /target/boot/initramfs-postmarketos-qcom-sm6350 \
                 /target/boot/initramfs-pmos \
                 /target/boot/initramfs; do
            if [ -f "$r" ]; then INITRAMFS="$r"; break; fi
        done
        if [ -z "$INITRAMFS" ]; then
            INITRAMFS="$(ls /target/boot/initramfs* 2>/dev/null | head -n 1 || true)"
        fi
        if [ -z "$INITRAMFS" ] || [ ! -f "$INITRAMFS" ]; then
            # Last resort: regenerate via postmarketos-mkinitfs in chroot.
            apk add --no-interactive postmarketos-mkinitfs >/dev/null 2>&1 || true
            chroot /target /sbin/mkinitfs >/dev/null 2>&1 || true
            for r in /target/boot/initramfs-postmarketos-qcom-sm6350 \
                     /target/boot/initramfs-pmos \
                     /target/boot/initramfs; do
                if [ -f "$r" ]; then INITRAMFS="$r"; break; fi
            done
        fi
        if [ -z "$INITRAMFS" ] || [ ! -f "$INITRAMFS" ]; then
            echo "ERROR: no initramfs under /target/boot/" >&2
            ls -la /target/boot/ >&2 || true
            exit 1
        fi
        echo "build-fairphone4: initramfs: $INITRAMFS"

        # Locate DTB. linux-postmarketos-qcom-sm6350 installs DTBs to
        # /usr/share/dtb/<deviceinfo_dtb>.dtb (the .dtb suffix is added
        # by the kernel apk on Alpine).
        DTB_PATH="/target/usr/share/dtb/${DEVICE_DTB}.dtb"
        if [ ! -f "$DTB_PATH" ]; then
            for cand in \
                "/target/boot/dtbs/${DEVICE_DTB}.dtb" \
                "/target/boot/dtbs-postmarketos-qcom-sm6350/${DEVICE_DTB}.dtb" \
                "/target/lib/firmware/dtbs/${DEVICE_DTB}.dtb"; do
                if [ -f "$cand" ]; then DTB_PATH="$cand"; break; fi
            done
        fi
        if [ ! -f "$DTB_PATH" ]; then
            echo "ERROR: DTB not found for ${DEVICE_DTB} (.dtb)" >&2
            find /target -name "*.dtb" -path "*sm7225-fairphone*" 2>/dev/null | sed "s|^|  candidate: |" >&2 || true
            exit 1
        fi
        echo "build-fairphone4: dtb: $DTB_PATH"

        # If deviceinfo says append-dtb, concatenate kernel+DTB into a
        # single image and feed that to mkbootimg. Otherwise pass the DTB
        # via --dt (mkbootimg-osm0sis).
        WORK=/tmp/atomos-bootimg
        mkdir -p "$WORK"
        cp "$KERNEL" "$WORK/kernel"
        if [ "$DEVICE_APPEND_DTB" = "true" ]; then
            cat "$DTB_PATH" >> "$WORK/kernel"
            DTARG=""
        else
            cp "$DTB_PATH" "$WORK/dtb"
            DTARG="--dt $WORK/dtb"
        fi
        cp "$INITRAMFS" "$WORK/initramfs"

        # mkbootimg-osm0sis flags. Keep --header_version 0 (FP4 uses the
        # legacy boot.img header, which is what deviceinfo offsets above
        # describe).
        # shellcheck disable=SC2086
        mkbootimg \
            --kernel "$WORK/kernel" \
            --ramdisk "$WORK/initramfs" \
            --base "$DEVICE_BASE" \
            --kernel_offset "$DEVICE_KOFF" \
            --ramdisk_offset "$DEVICE_ROFF" \
            --second_offset "$DEVICE_SOFF" \
            --tags_offset "$DEVICE_TOFF" \
            --pagesize "$DEVICE_PAGESIZE" \
            --cmdline "$DEVICE_KERNEL_CMDLINE" \
            $DTARG \
            --output /exportdir/boot.img
        ls -la /exportdir/boot.img
    '

##############################################################################
# Step 6.5: pre-pack assertion. Verify the rootfs we are ABOUT to rsync
# into the sparse image still has everything greetd needs at boot.
#
# Step 5 already verified state right after AtomOS overlays applied, but
# nothing between Step 5 and Step 7 should mutate /etc/passwd or the
# runlevel symlinks. If this assertion ever fails, something silently
# regressed the rootfs after final-verify -- catch it here instead of
# shipping a broken sparse image and watching the device boot loop.
##############################################################################
echo "=== build-fairphone4: pre-pack rootfs assertion ==="
"$ENGINE" run --rm --platform "linux/arm64" \
    -v "$ROOTFS_VOLUME:/target:ro" \
    "$ALPINE_IMAGE" /bin/sh -eu -c '
        FAIL=0
        echo "pre-pack: /etc/passwd greetd row:"
        if grep "^greetd:" /target/etc/passwd; then
            :
        else
            echo "  FAIL: greetd not found in /target/etc/passwd" >&2
            FAIL=1
        fi
        echo "pre-pack: /etc/group greetd row:"
        if grep "^greetd:" /target/etc/group; then
            :
        else
            echo "  FAIL: greetd group not found in /target/etc/group" >&2
            FAIL=1
        fi
        echo "pre-pack: runlevel default symlinks (greetd / seatd / elogind):"
        for svc in greetd seatd elogind; do
            target=/target/etc/runlevels/default/$svc
            if [ -L "$target" ]; then
                echo "  ok   $svc -> $(readlink "$target")"
            elif [ -e "$target" ]; then
                echo "  ok   $svc (regular file present)"
            else
                echo "  FAIL $svc missing under /etc/runlevels/default/" >&2
                FAIL=1
            fi
        done
        if [ "$FAIL" -ne 0 ]; then
            echo "FATAL: pre-pack rootfs assertion failed; refusing to build a broken sparse image." >&2
            echo "       Listing /target/etc/runlevels/default/ for diagnostics:" >&2
            ls -la /target/etc/runlevels/default/ >&2 || true
            exit 14
        fi
        echo "pre-pack: rootfs ready for packing."
    '

##############################################################################
# Step 7: pack rootfs into sparse fairphone-fp4.img (ext4 + img2simg).
##############################################################################
echo "=== build-fairphone4: pack rootfs into sparse image ==="
"$ENGINE" run --rm --privileged --platform "linux/arm64" \
    -v "$ROOTFS_VOLUME:/target:ro" \
    -v "$EXPORT_DIR:/exportdir" \
    -e PROFILE_NAME="$PROFILE_NAME" \
    -e ATOMOS_FP4_ROOTFS_SIZE_MB="${ATOMOS_FP4_ROOTFS_SIZE_MB:-}" \
    -e DEVICE_FLASH_SPARSE="$DEVICE_FLASH_SPARSE" \
    "$ALPINE_IMAGE" /bin/sh -eu -c '
        # Intentionally NOT installing GNU coreutils here. Alpines edge
        # ships coreutils built against a newer musl symbol set than the
        # alpine:edge container image typically carries (the GNU rm fails
        # at load time with "Error relocating /bin/rm: renameat2: symbol
        # not found" when musl is older than 1.2.3+). Busybox supports
        # every option this script uses (truncate -s NM, du -sk, rm -f,
        # ls -la), so coreutils is unnecessary for the packing stage. The
        # rootfs itself still has GNU coreutils because boot-deploy needs
        # it in the chroot; that install path is unaffected.
        apk add --no-interactive e2fsprogs util-linux mount rsync android-tools >/dev/null
        RAW=/exportdir/${PROFILE_NAME}.raw.img
        SPARSE=/exportdir/${PROFILE_NAME}.img
        rm -f "$RAW" "$SPARSE"

        if [ -n "${ATOMOS_FP4_ROOTFS_SIZE_MB:-}" ]; then
            SIZE_MB="$ATOMOS_FP4_ROOTFS_SIZE_MB"
        else
            # Used + 256 MiB headroom (free space for first-boot + apk add).
            USED_KB="$(du -sk /target | cut -f1)"
            SIZE_MB=$(( (USED_KB / 1024) + 256 ))
            # Round up to nearest 64 MiB.
            REM=$(( SIZE_MB % 64 ))
            if [ "$REM" -ne 0 ]; then SIZE_MB=$(( SIZE_MB + 64 - REM )); fi
        fi
        echo "build-fairphone4: rootfs raw image size = ${SIZE_MB} MiB"

        # Allocate sparse file and create ext4 inside it.
        truncate -s "${SIZE_MB}M" "$RAW"
        # Filesystem label MUST be "pmOS_root". The postmarketos-initramfs
        # init script (pmaports/main/postmarketos-initramfs/init_functions.sh
        # find_root_partition) locates the rootfs via:
        #   PMOS_ROOT="$(find_partition "$root_uuid" "$root_path" "pmOS_root" ...)"
        # which falls back to `blkid --label pmOS_root`. With any other
        # label, the device boots into "Waiting for root partition..."
        # forever because blkid returns no match.
        mkfs.ext4 -F -L pmOS_root "$RAW"

        mkdir -p /mnt/root
        mount -o loop "$RAW" /mnt/root
        rsync -aHAX --delete --numeric-ids /target/ /mnt/root/
        sync
        umount /mnt/root

        if [ "${DEVICE_FLASH_SPARSE:-true}" = "true" ]; then
            echo "build-fairphone4: converting raw image to sparse via img2simg"
            img2simg "$RAW" "$SPARSE"
            rm -f "$RAW"
        else
            mv "$RAW" "$SPARSE"
        fi
        ls -la "$SPARSE"
    '

# Best-effort export ownership normalize (matches build-qemu).
if [ "${ATOMOS_SKIP_EXPORT_CHOWN:-0}" != "1" ]; then
    if ! "$ENGINE" run --rm \
        -v "$EXPORT_DIR:/export" \
        "$ALPINE_IMAGE" /bin/sh -eu -c "chown -R $HOST_UID:$HOST_GID /export" >/dev/null 2>&1; then
        echo "Note: skipped export ownership adjustment (expected on some rootless runtimes)."
    fi
fi

