if [ "${ATOMOS_FP4_MESON_CACHE_CLEAN:-0}" = "1" ]; then
    echo "build-fairphone4: ATOMOS_FP4_MESON_CACHE_CLEAN=1 -> wiping cache ($MESON_CACHE_KIND)"
    if [ -n "$MESON_CACHE_HOST_DIR" ] && [ -d "$MESON_CACHE_HOST_DIR" ]; then
        "$ENGINE" run --rm -v "$MESON_CACHE_HOST_DIR:/cache" \
            "$ALPINE_IMAGE" /bin/sh -c 'rm -rf /cache/* /cache/.[!.]* 2>/dev/null || true' \
            >/dev/null 2>&1 || rm -rf "$MESON_CACHE_HOST_DIR"
        mkdir -p "$MESON_CACHE_HOST_DIR"
    elif [ -z "$MESON_CACHE_HOST_DIR" ]; then
        "$ENGINE" volume rm -f "$MESON_CACHE_VOLUME" >/dev/null 2>&1 || true
        "$ENGINE" volume create "$MESON_CACHE_VOLUME" >/dev/null
    fi
fi
echo "build-fairphone4: meson cache backend: $MESON_CACHE_KIND"

cleanup_volume() {
    # FP4-specific keep-volume opt-out (lets `make build-fairphone4` reuse a
    # bootstrapped Alpine arm64 rootfs across iterations -- saves ~3 min of
    # apk install on each rebuild). The actual volume removal is delegated
    # to atomos_cleanup_volume() in scripts/_lib-build-common.sh; this
    # wrapper just adds the gate.
    if [ "${ATOMOS_FP4_KEEP_ROOTFS_VOLUME:-0}" = "1" ]; then
        echo "build-fairphone4: keeping rootfs volume $ROOTFS_VOLUME (ATOMOS_FP4_KEEP_ROOTFS_VOLUME=1)"
        return 0
    fi
    atomos_cleanup_volume "$ENGINE" "$ROOTFS_VOLUME"
}
trap cleanup_volume EXIT
cleanup_volume
"$ENGINE" volume create "$ROOTFS_VOLUME" >/dev/null

##############################################################################
# Step 1: bootstrap Alpine + pmOS rootfs into the docker volume.
##############################################################################
echo "=== build-fairphone4: bootstrap Alpine + pmOS rootfs (arch=$DEVICE_ARCH) ==="

# Same approach as build-qemu's BASE_APK_PACKAGES, but tuned for the FP4
