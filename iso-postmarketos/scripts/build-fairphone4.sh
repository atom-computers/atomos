echo "Build complete:"
echo "  $BOOT_IMG_PATH"
echo "  $ROOTFS_IMG_PATH"
echo ""
echo "Flash with fastboot (device in bootloader):"
echo "  fastboot flash boot     $BOOT_IMG_PATH"
echo "  fastboot flash userdata $ROOTFS_IMG_PATH"
