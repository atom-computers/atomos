# Step 6: export boot.img.
#
# Prefer the artifact produced by boot-deploy in Step 1.5 (it knows the
# device's exact mkbootimg invocation, header version, signing
# requirements, etc.). Fall back to a hand-rolled mkbootimg using the
# deviceinfo offsets only when boot-deploy did not deposit a usable
