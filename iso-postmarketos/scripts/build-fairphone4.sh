# Profile extras + parity candidates (same merging logic as build-qemu).
PROFILE_EXTRA_CSV="${PMOS_EXTRA_PACKAGES:-}"
PARITY_CSV="${PMOS_PARITY_PACKAGE_CANDIDATES:-}"

PACKAGE_MANIFEST="$(
    python3 - "$PROFILE_EXTRA_CSV" "$PARITY_CSV" "${BASE_APK_PACKAGES[*]}" "${PMOS_APK_PACKAGES[*]}" <<'PY'
import sys

profile = [p.strip() for p in sys.argv[1].split(",") if p.strip()]
parity = [p.strip() for p in sys.argv[2].split(",") if p.strip()]
base = [p.strip() for p in sys.argv[3].split() if p.strip()]
pmos = [p.strip() for p in sys.argv[4].split() if p.strip()]

# Map a few legacy names that creep in from older configs.
replacements = {
    "webkit6.0-gtk4-dev": "webkit2gtk-6.0-dev",
}
ordered = []
for group in (base, pmos, profile, parity):
    for p in group:
        p = replacements.get(p, p)
        if not p:
            continue
        if p not in ordered:
            ordered.append(p)
print(" ".join(ordered))
PY
)"

echo "Effective APK package set:"
echo "  $PACKAGE_MANIFEST"

"$ENGINE" run --rm --platform "linux/arm64" \
    -v "$ROOTFS_VOLUME:/target" \
    -v "$REPOSITORIES_FILE:/tmp/repositories:ro" \
    -v "$PMOS_KEY_HOST:/tmp/pmos.rsa.pub:ro" \
    "$ALPINE_IMAGE" /bin/sh -eu -c "
        mkdir -p /target/etc/apk/keys
        cp -r /etc/apk/keys/. /target/etc/apk/keys/
        # Trust the postmarketOS signing key in BOTH the host apk DB (so
        # apk --root /target add can verify pmOS index signatures during
        # bootstrap) AND in the target rootfs (so on-device apk continues
        # to trust the same mirror).
        cp /tmp/pmos.rsa.pub /etc/apk/keys/build.postmarketos.org.rsa.pub
        cp /tmp/pmos.rsa.pub /target/etc/apk/keys/build.postmarketos.org.rsa.pub
        cp /tmp/repositories /target/etc/apk/repositories
        cp /tmp/repositories /etc/apk/repositories
        apk update >/dev/null
        set +e
        apk --root /target \
            --initdb \
            --keys-dir /target/etc/apk/keys \
            --repositories-file /target/etc/apk/repositories \
            --update-cache \
            --no-interactive \
            add $PACKAGE_MANIFEST >/tmp/apk-manifest.log 2>&1
        rc_manifest=\$?
        set -e
        cat /tmp/apk-manifest.log
        if [ \"\$rc_manifest\" -ne 0 ]; then
            echo \"WARN: full manifest install returned non-zero (\$rc_manifest); retrying base set...\" >&2
        fi
        # Retry pass with just the BASE + PMOS sets so a missing optional
        # package (parity / profile extra) doesn't sink the whole bootstrap.
        set +e
        apk --root /target \
            --keys-dir /target/etc/apk/keys \
            --repositories-file /target/etc/apk/repositories \
            --no-interactive \
            add ${BASE_APK_PACKAGES[*]} ${PMOS_APK_PACKAGES[*]} >/tmp/apk-base.log 2>&1
        rc=\$?
        set -e
        cat /tmp/apk-base.log
        if [ \"\$rc\" -ne 0 ]; then
            # apk returns non-zero when ANY post-install or trigger script
            # fails, even though all packages installed correctly. The
            # postmarketos-mkinitfs trigger commonly fails on first
            # bootstrap because /usr/sbin/losetup does not exist yet
            # (Alpine ships it at /sbin/losetup; pmOS hook expects
            # /usr/sbin/losetup). The post-bootstrap fixup step below
            # symlinks the binary and re-runs mkinitfs, so this trigger
            # failure is recoverable. Distinguish recoverable trigger
            # failures from true package install errors.
            non_trigger_errors=\$(
                grep -E '^ERROR:' /tmp/apk-base.log \
                | grep -Ev 'apk/exec/postmarketos-mkinitfs-[^ ]+\\.trigger:' \
                || true
            )
            if [ -n \"\$non_trigger_errors\" ]; then
                echo \"ERROR: base+pmOS package install failed (\$rc) with non-trigger errors:\" >&2
                printf '%s\\n' \"\$non_trigger_errors\" >&2
                exit \"\$rc\"
            fi
            echo \"NOTE: apk exited \$rc but only postmarketos-mkinitfs trigger errors were reported; will regenerate initramfs in post-bootstrap fixup.\" >&2
        fi
        # Hard fail only when bootstrap produced an obviously unusable rootfs.
        test -x /target/bin/sh
        test -x /target/usr/sbin/sshd
        test -x /target/usr/bin/mkbootimg
        test -d /target/lib/modules
    "

##############################################################################
# Step 1.5: post-bootstrap mkinitfs fixup.
#
# Alpine ships losetup at /sbin/losetup; postmarketos-mkinitfs hook
# 00-initramfs-base.files lists /usr/sbin/losetup. Without the symlink
# below, the trigger fails with:
#   hookfiles: unable to add "/usr/sbin/losetup": getFile: failed to stat
#   file "/usr/sbin/losetup": stat /usr/sbin/losetup: no such file or
#   directory
# The pmbootstrap path side-steps this by patching the hook .files in
# its pmaports cache (apply_mkinitfs_*_compat_in_pmaports_cache); we
# cannot do that with binary apks, so we fix the path in the rootfs and
# re-run mkinitfs. Hard-fails if no initramfs lands in /target/boot/.
##############################################################################
echo "=== build-fairphone4: post-bootstrap mkinitfs + boot-deploy fixup ==="
"$ENGINE" run --rm --privileged --platform "linux/arm64" \
    -v "$ROOTFS_VOLUME:/target" \
    "$ALPINE_IMAGE" /bin/sh -eu -c '
        # ---- losetup path fix --------------------------------------------
        # Ensure /usr/sbin/losetup exists. losetup package installs to
        # /sbin/losetup on Alpine edge regardless of usrmerge state.
        if [ ! -e /target/usr/sbin/losetup ]; then
            if [ -e /target/sbin/losetup ]; then
                mkdir -p /target/usr/sbin
                ln -sf ../../sbin/losetup /target/usr/sbin/losetup
                echo "fixup: created /usr/sbin/losetup -> /sbin/losetup"
            else
                echo "ERROR: /target/sbin/losetup is missing; losetup apk did not install correctly." >&2
                ls -la /target/sbin/ /target/usr/sbin/ 2>/dev/null | head -40 >&2 || true
                exit 1
            fi
        fi

        # ---- mkinitfs hook compat patches --------------------------------
        # Same compat patches the pmbootstrap path applies to the pmaports
        # cache (apply_mkinitfs_*_compat_in_pmaports_cache) but applied to
        # the *installed* hook files in the rootfs since we use binary apks.
        for f in /target/usr/share/mkinitfs/files/*.files \
                 /target/etc/mkinitfs/files/*.files; do
            [ -f "$f" ] || continue
            if grep -q "udev/udev.conf" "$f" 2>/dev/null; then
                sed -i "/udev\\/udev\\.conf/d" "$f"
                echo "fixup: patched udev.conf reference out of $f"
            fi
        done

        # ---- chroot bind mounts ------------------------------------------
        # boot-deploy (called by postmarketos-mkinitfs as the deploy stage)
        # invokes:
        #   df --output=avail <tempdir>            -> needs /proc/mounts
        #   bc -l (multi-char vars like "available") -> needs GNU bc
        # Without /proc bind-mounted into the chroot, df fails with
        # "can'\''t find mount point". Without GNU bc, the script
        # short-circuits to "sh: invalid number ''\'\'\''". coreutils + bc
        # are now in the bootstrap manifest; the missing piece is the
        # virtual filesystems.
        for fs in proc sys dev dev/pts; do
            mkdir -p /target/$fs
        done
        mount -t proc proc        /target/proc
        mount -t sysfs sysfs      /target/sys
        mount --bind /dev         /target/dev
        mount --bind /dev/pts     /target/dev/pts 2>/dev/null || mount -t devpts devpts /target/dev/pts
        cleanup() {
            umount /target/dev/pts 2>/dev/null || true
            umount /target/dev     2>/dev/null || true
            umount /target/sys     2>/dev/null || true
            umount /target/proc    2>/dev/null || true
        }
        trap cleanup EXIT INT TERM

        # ---- regenerate initramfs (and trigger boot-deploy) --------------
        # Force /usr/bin first so chroot picks up GNU coreutils (df) and
        # GNU bc (multi-char variable names) ahead of the busybox shadows
        # in /bin and /sbin.
        echo "fixup: regenerating initramfs"
        if [ -x /target/sbin/postmarketos-mkinitfs ] || [ -x /target/usr/sbin/postmarketos-mkinitfs ]; then
            chroot /target /usr/bin/env PATH=/usr/bin:/usr/sbin:/sbin:/bin postmarketos-mkinitfs
        else
            chroot /target /usr/bin/env PATH=/usr/bin:/usr/sbin:/sbin:/bin mkinitfs
        fi

        # ---- post-mkinitfs assertions ------------------------------------
        if ! ls /target/boot/initramfs* >/dev/null 2>&1; then
            echo "ERROR: post-fixup mkinitfs run did not produce any initramfs in /target/boot/." >&2
            ls -la /target/boot/ >&2
            exit 2
        fi
        echo "fixup: initramfs files now under /target/boot/:"
        ls -la /target/boot/initramfs* >&2
        # boot-deploy normally also drops boot.img-<flavor> + a
        # combined boot.img into /target/boot/; report what it did so
        # Step 6 can pick it up directly.
        if ls /target/boot/boot.img* >/dev/null 2>&1; then
            echo "fixup: boot-deploy produced boot.img file(s):"
            ls -la /target/boot/boot.img* >&2
        else
            echo "fixup: WARN no boot.img under /target/boot/ after mkinitfs; Step 6 will assemble manually." >&2
        fi
    '

